    DefaultTableCellRenderer tableCellRenderer = new DefaultTableCellRenderer() {

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component comp = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            Object ref = table.getValueAt(row, 2);
            //row : Linha genérica (vai retornar quantas linhas existe no JTable e valida uma por uma) 2 : valor do campo "valor"
            if (ref != null && (Float.parseFloat(ref.toString()) > 0)) {
                comp.setForeground(Color.green);
            } else if (ref != null && (Float.parseFloat(ref.toString()) == 0)) {
                comp.setForeground(Color.black);
            } else {
                comp.setForeground(Color.red);
            }
            return comp;
        }
    };

    /**
     *
     * @param lancamento
     */
    public void adicionarValorTabela(Object lancamento) {
        String dataFormatada;
        Object[] linha;
        linha = new Object[4];
        DateTimeFormatter formatarData = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        if (lancamento instanceof Receita) {
            linha[0] = ((Receita) lancamento).getTipoLancamento();//tipo lançamento
            linha[1] = ((Receita) lancamento).getTipoReceita().toString();//tipo Receita
            linha[2] = "+" + ((Receita) lancamento).getValor();//valor   
            jtTabela.setDefaultRenderer(Object.class, tableCellRenderer);
            dataFormatada = formatarData.format(((Receita) lancamento).getData());
            linha[3] = dataFormatada;//data
        } else {
            linha[0] = ((Despesa) lancamento).getTipoLancamento();//tipo lançamento
            linha[1] = ((Despesa) lancamento).getTipoDespesa().toString();
            linha[2] = "-" + ((Despesa) lancamento).getValor();//valor
            jtTabela.setDefaultRenderer(Object.class, tableCellRenderer);
            dataFormatada = formatarData.format(((Despesa) lancamento).getData());
            linha[3] = dataFormatada;//data            
        }
        DefaultTableModel model = (DefaultTableModel) jtTabela.getModel();
        model.addRow(linha);
    }

