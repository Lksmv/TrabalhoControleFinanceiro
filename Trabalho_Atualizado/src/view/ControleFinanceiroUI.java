    private void jbInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInserirActionPerformed
        // TODO add your handling code here:

        try {
            String data = tfData.getText();//armazena o campo data
            DateTimeFormatter formatar = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate date = LocalDate.parse(data, formatar);

            String tipoLancamento;

            if (rbDespesa.isSelected()) {

                tipoLancamento = "Despesa";
                Despesa d = new Despesa(tfValor.getText(), tipoDespesa(), date, tipoLancamento);

                control.incluirLancamento(d);

                adicionarValorTabela(d);

            } else if (rbReceita.isSelected()) {

                tipoLancamento = "Receita";

                Receita r = new Receita(tfValor.getText(), tipoReceita(), date, tipoLancamento);

                control.incluirLancamento(r);

                adicionarValorTabela(r);

            }

            NumberFormat nfBrasil = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));

            String saldoAtualFormatado = nfBrasil.format(control.calcularSaldo(true));
            String saldoTotalFormatado = nfBrasil.format(control.calcularSaldo(false));
            jlSaldoAtual.setText("Saldo Atual : " + saldoAtualFormatado);
            jlSaldoTotal.setText("Saldo Total : " + saldoTotalFormatado);

            tfValor.setText("");
            tfData.setText("");

        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "O campo 'Data' está inválido, favor preencher corretamente.");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "O campo 'Valor' está inválido, favor preencher corretamente.");
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi selecionado um tipo, favor selecionar um tipo.");
            return;

        }

    }//GEN-LAST:event_jbInserirActionPerformed

