    public TipoDespesa tipoDespesa() {
        if (rbAlimentacao.isSelected()) {
            return TipoDespesa.ALIMENTACAO;
        } else if (rbTransporte.isSelected()) {
            return TipoDespesa.TRANSPORTE;
        } else if (rbResidencia.isSelected()) {
            return TipoDespesa.RESIDENCIA;
        } else if (rbSaude.isSelected()) {
            return TipoDespesa.SAUDE;
        } else if (rbEntretenimento.isSelected()) {
            return TipoDespesa.ENTRETENIMENTO;
        } else if (rbOutrosD.isSelected()) {
            return TipoDespesa.OUTRAS;
        } else if (rbEducacao.isSelected()) {
            return TipoDespesa.EDUCACAO;
        } else {
            return null;
        }

    }

